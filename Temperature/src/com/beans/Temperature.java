package com.beans;

public class Temperature {
	
	
	private int temp;

	public Temperature(int temp) {
		super();
		this.temp = temp;
	}

	public Temperature() {
		super();
	}

	public int getTemp() {
		return temp;
	}

	public void setTemp(int temp) {
		this.temp = temp;
	}
	
	

}
